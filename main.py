@app.on_message(filters.command("bio"))
async def bio_handler(_, message: Message):
    if len(message.command) < 2:
        return await message.reply_text(
            "Usage:\n/bio your text"
        )

    raw_text = " ".join(message.command[1:])

    out = "𓆩 𝐁ɪᴏ ꜱᴛʏʟᴇ 𓆪\n\n"

    # Apply each template with different font styles
    for idx, (pre, suf) in enumerate(BIO_TEMPLATES):
        # Use different font maps for variety
        font_map = BIO_FONT_MAPS[idx % len(BIO_FONT_MAPS)]
        fancy_text = bio_convert(raw_text, font_map)
        out += f"{pre}{fancy_text}{suf}\n\n"

    await message.reply_text(out)

